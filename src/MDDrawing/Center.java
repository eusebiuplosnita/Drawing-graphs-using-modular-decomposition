package MDDrawing;

/**
 *
 * @author Daniel
 */
public class Center {
    
    private double x;
    private double y;
    
    public Center(double xi, double yi)
    {
        this.x = xi;
        this.y = yi;
    }

    /**
     * @return the x
     */
    public double getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(double x) {
        this.x = x;
    }

    /**
     * @return the y
     */
    public double getY() {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(double y) {
        this.y = y;
    }
    
}
